package com.example.exem

object ExcuseRepository {

    enum class Category(val title: String) {
        STUDY("Учёба"),
        WORK("Работа"),
        FAMILY("Семья")
    }

    private val data = mapOf(
        Category.STUDY to listOf(
            "Забыл(а) тетрадь дома — собирался(ась) в спешке.",
            "Сел(а) делать, но интернет пропал.",
            "Отправил(а) не тот файл — сейчас пришлю правильный.",
            "Думал(а), дедлайн завтра…",
            "Сильно разболелась голова — не смог(ла) сосредоточиться."
        ),
        Category.WORK to listOf(
            "Застрял(а) в пробке, подъезжаю.",
            "Созвон затянулся дольше, чем планировали.",
            "Сломалась сборка/CI — чиню.",
            "Согласование заняло больше времени, чем ожидалось.",
            "Был(а) на срочной задаче — переключаюсь сейчас."
        ),
        Category.FAMILY to listOf(
            "Помогал(а) дома — срочно попросили.",
            "Нужно было отвезти/забрать родственника.",
            "Случились внезапные семейные дела.",
            "Не видел(а) уведомление — был(а) занят(а) дома.",
            "Отключали свет — не мог(ла) ответить."
        )
    )

    fun random(category: Category): String {
        val list = data[category].orEmpty()
        return if (list.isNotEmpty()) list.random() else "Пока нет отговорок в этой категории."
    }
}
